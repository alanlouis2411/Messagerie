    private void btConfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConfActionPerformed
        String mat = tfMat.getText();
        Connection dbConnect = DBConnection.getConnection();
       if (dbConnect == null) {
           System.exit(1);
       }
       if (mat != null){
            try {
                stmt = dbConnect.createStatement();
            } catch (SQLException ex) {
                Logger.getLogger(MenuEmp.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                rs = stmt.executeQuery("select * from employe");
            } catch (SQLException ex) {
                Logger.getLogger(MenuEmp.class.getName()).log(Level.SEVERE, null, ex);
            }
            flag = 0;
            try {
                while(rs.next()){
                    if(rs.getString("MATRICULE").equals(mat)){
                        flag = 1;
                        nom = rs.getString("NOM");
                        prenom = rs.getString("PRENOM");
                        idemp = rs.getInt("IDEMP");
                        break;
                    }
                }
            } catch (SQLException ex) {
                Logger.getLogger(MenuEmp.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(flag == 0){
                JOptionPane.showMessageDialog(this,"Le matricule entr√© n'existe pas !","ERREUR",JOptionPane.INFORMATION_MESSAGE);
                Fenetre.f.setContentPane(new MenuEmp());
                Fenetre.f.repaint();
                Fenetre.f.revalidate();
            }
            else{
                Fenetre.f.setContentPane(new MenuEmp2());
                Fenetre.f.repaint();
                Fenetre.f.revalidate();
            }
       }
       else{
           JOptionPane.showMessageDialog(this,"Le champ est vide !","ERREUR",JOptionPane.INFORMATION_MESSAGE);
           Fenetre.f.setContentPane(new MenuEmp());
           Fenetre.f.repaint();
           Fenetre.f.revalidate();
       }
       DBConnection.closeConnection();
    }//GEN-LAST:event_btConfActionPerformed

